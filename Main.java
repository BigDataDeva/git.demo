public class Main {
    public static void main(String[] args) {
        // Declare and initialize an array
        int[] myArray = {1, 2, 3, 4, 5};

        // Print each element of the array
        for (int i = 0; i < myArray.length; i++) {
            System.out.println("Element at index " + i + ": " + myArray[i]);
        }
    }
}
